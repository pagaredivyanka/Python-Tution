#1. From String(str) to Integer(int)

x = "123"
y = int(x)
print(y) # 123
print(type(y)) # <class 'int'>


#2. Float(float) to Integer(int)

x = 56.901
y = int(x)
print(x) # 56.901
print(type(x))  # <class 'float'>
print(y) # 56
print(type(y))  # <class 'int'>

#3. Boolean(bool) to Integer(int)

x = True
y = int(x)

print(x)
print(type(x)) # <class 'bool'>
print(y) # 1
print(type(y)) # <class 'int'>




