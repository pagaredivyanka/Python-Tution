#1. Integer(int) to String(str)

x = 100 
y = str(x) 

print(x) # 100
print(type(x)) # <class 'int'>

print(y) 
print(type(y)) # <class 'str'>

#----------------------------------------------

#2. Float(float) to String(str)

x = 100.50
y = str(x)

print(x) # 100.50
print(type(x)) # <class 'float'>

print(y) # 100.50
print(type(y)) # <class'str'>

#3. Boolean(bool) to String(str)

x = True

print(x) # True
print(type(x)) # <class 'bool'>
y = str(x)

print(y) # True
print(type(y)) # <class 'str'> 


