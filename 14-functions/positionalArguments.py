# Defination - 
# Positional arguments are passed in the order in which they are defined.

# greeting
def greet(name, age):
    print(f"Hello {name}, you are {age} years old.")
greet("Divyanka", 20)


# addition

def add(x, y):
    return x + y
result = add(5, 10)   # X = 5 , Y = 10
print(result)

